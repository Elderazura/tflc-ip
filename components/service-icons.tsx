const serviceIconMap = {
  "intellectual-property-services": IconIP,
  "trademark-registration-brand-protection": IconTrademark,
  "patent-filing-strategy-commercialisation": IconPatent,
  "copyrights-designs-industrial-models": IconCopyright,
  "ip-audits-due-diligence": IconAudit,
  "our-approach-to-intellectual-property": IconApproach,
} as const;

export function getServiceIcon(href: string) {
  const key = href.replace("/services/", "") as keyof typeof serviceIconMap;
  return serviceIconMap[key] || IconIP;
}

const processIconMap = [IconConsult, IconStrategy, IconFile, IconEnforce] as const;
export function getProcessIcon(step: number) {
  return processIconMap[step - 1] || IconConsult;
}

const industryIconMap = [IconLuxury, IconTech, IconLifeScience, IconFMCG, IconArts, IconFinance] as const;
export function getIndustryIcon(index: number) {
  return industryIconMap[index] ?? IconLuxury;
}
